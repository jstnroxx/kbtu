# Text must be in quotes, no matter double or singular.
print("This will work!")
print('This will also work!')

# By default, print ends with a new line. With "end" parameter we can specify the symbol for the print to end with.
print("Hello World!", end=" ")
print("I will print on the same line.")

# Numbers do not require quotes when printing.
print("I am", 35, "years old.")