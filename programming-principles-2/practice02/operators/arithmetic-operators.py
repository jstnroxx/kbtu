# Arithmetic operators are used with numeric values to perform common mathematical operations.
"""
+	Addition	x + y	
-	Subtraction	x - y	
*	Multiplication	x * y	
/	Division	x / y	
%	Modulus	x % y	
**	Exponentiation	x ** y	
//	Floor division	x // y
"""

# Examples.
x = 15
y = 4

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)
print(x ** y)
print(x // y)

# Division always returns a float.
x = 12
y = 5

print(x / y)

# Floor division always returns an integer. It rounds DOWN to the nearest integer.
x = 12
y = 5

print(x // y)